# 有三个变量,分别为: a = 100, b = 200, c = 300,
# 现需要将这三个变量值进行交换,将a,b,c的值分别赋值给c,a,b,并将其输出到控制台

a = 100
b = 200
c = 300

# 使用临时变量 d 完成循环移位: a->c, b->a, c->b
d = c  # d = 300 (保存c的原始值)
c = a  # c = 100 (a的值赋给c)
a = b  # a = 200 (b的值赋给a)
b = d  # b = 300 (d中保存的c原始值赋给b)

print(c, a, b)  # 输出: c=100, a=200, b=300


