## 集合
"""
去重：列表有重复元素，转成集合自动去掉
集合运算：求两个群体的交集、并集、差集
"""
## 基本操作

# 创建
s1 = {1, 2, 3}
s2 = set()  # ⚠️ 空集合不能用 {}，那是空字典！
s3 = set([1, 2, 2, 3])  # 列表转集合，自动去重 → {1, 2, 3}

# 增删
s1.add(4)  # 添加元素
s1.remove(1)  # 删除，不存在会报错
s1.discard(99)  # 删除，不存在不报错（更安全）

# 判断
print(f"3 in s1 is {3 in s1}")  # True
print(s1)

## 集合运算（重点！）
a = {1, 2, 3, 4}
b = {3, 4, 5, 6}

print(a | b)   # 并集：{1, 2, 3, 4, 5, 6}  两个都要
print(a & b)   # 交集：{3, 4}              两个都有的
print(a - b)   # 差集：{1, 2}              只在a里有的
print(a ^ b)   # 对称差：{1, 2, 5, 6}      只在其中一个里的

